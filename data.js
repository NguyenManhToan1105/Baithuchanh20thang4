export const searchCategories = ['Eggs', 'Noodles & Pasta', 'Chips & Crisps', 'Fast Food', 'Beverages'];
export const brands = ['Individual Collection', 'Cocola', 'Ifad', 'Kazi Farms'];

export function getProductsByCategory(category) {
  return products.filter((product) => product.category === category);
}

export function searchProducts(query = '', filters = {}) {
  const normalizedQuery = query.trim().toLowerCase();
  const selectedCategories = filters.categories || [];
  const selectedBrands = filters.brands || [];

  return products.filter((product) => {
    const matchesQuery =
      !normalizedQuery ||
      product.name.toLowerCase().includes(normalizedQuery) ||
      product.category.toLowerCase().includes(normalizedQuery) ||
      product.brand.toLowerCase().includes(normalizedQuery) ||
      product.keywords.some((keyword) => keyword.toLowerCase().includes(normalizedQuery));

    const matchesCategory =
      selectedCategories.length === 0 || selectedCategories.includes(product.category);
    const matchesBrand = selectedBrands.length === 0 || selectedBrands.includes(product.brand);

    return matchesQuery && matchesCategory && matchesBrand;
  });
}
